import { execFileSync } from "node:child_process";
import { existsSync, readFileSync } from "node:fs";
import path from "node:path";
import { pathToFileURL } from "node:url";
import {
  candidateUpstreamCounterpartPaths,
  classifyPrefixedLocResult,
  countNonEmptyLines,
  matchesAnyGlob,
  shouldCheckLoc,
  toSet,
} from "./t3work-additive-guard-lib.mjs";
import { maybeCheckWhitelistedAutoMerge } from "./t3work-additive-guard-merge.mjs";

const LOC_WARN_THRESHOLD = 150;
const LOC_FAIL_THRESHOLD = 200;

function runGit(args) {
  return execFileSync("git", args, { encoding: "utf8" }).trim();
}

function maybeRunGit(args) {
  try {
    return runGit(args);
  } catch {
    return null;
  }
}

function assertBaseRef(baseRef) {
  const candidates = [baseRef, "origin/main", "main"];
  for (const candidate of candidates) {
    const ok = maybeRunGit(["rev-parse", "--verify", candidate]);
    if (ok) return candidate;
  }

  throw new Error(
    `Could not resolve base ref '${baseRef}' or fallback refs 'origin/main'/'main'. Fetch remotes and try again.`,
  );
}

function loadConfig(cwd) {
  const configPath = path.join(cwd, ".t3work-additive-guard.json");
  if (!existsSync(configPath)) {
    throw new Error(`Missing config file: ${configPath}`);
  }

  const raw = readFileSync(configPath, "utf8");
  const parsed = JSON.parse(raw);
  const expectedRule =
    "DO NOT EDIT THIS FILE MANUALLY. Update allowlists via controlled migration changes only.";

  if (parsed.guardFileRule !== expectedRule) {
    throw new Error(
      "Invalid .t3work-additive-guard.json guardFileRule. DO NOT EDIT THIS FILE MANUALLY.",
    );
  }

  return {
    baseRef: parsed.baseRef ?? "upstream/main",
    requiredPrefixes: parsed.requiredPrefixes ?? [parsed.requiredPrefix ?? "t3work-"],
    locWarnThreshold: parsed.locWarnThreshold ?? LOC_WARN_THRESHOLD,
    locFailThreshold: parsed.locFailThreshold ?? LOC_FAIL_THRESHOLD,
    allowedModifiedFiles: parsed.allowedModifiedFiles ?? [],
    allowedModifiedFileGlobs: parsed.allowedModifiedFileGlobs ?? [],
    allowedUnprefixedNewFiles: parsed.allowedUnprefixedNewFiles ?? [],
  };
}

function resolveUpstreamCounterpartPath(baseRef, filePath, requiredPrefixes) {
  const candidates = candidateUpstreamCounterpartPaths(filePath, requiredPrefixes);
  for (const candidate of candidates) {
    if (fileExistsInRef(baseRef, candidate)) {
      return candidate;
    }
  }
  return null;
}

function fileExistsInRef(ref, filePath) {
  const listed = maybeRunGit(["ls-tree", "-r", "--name-only", ref, "--", filePath]);
  return listed?.split("\n").includes(filePath) ?? false;
}

function isGitIgnored(filePath) {
  return maybeRunGit(["check-ignore", filePath]) !== null;
}

function collectCandidatePaths(mergeBase) {
  const committedOrStagedOrUnstaged = maybeRunGit([
    "diff",
    "--name-only",
    "--diff-filter=ACMR",
    mergeBase,
    "--",
  ]);
  const unstaged = maybeRunGit(["diff", "--name-only", "--diff-filter=ACMR", "--"]);
  const staged = maybeRunGit(["diff", "--cached", "--name-only", "--diff-filter=ACMR", "--"]);
  const untracked = maybeRunGit(["ls-files", "--others", "--exclude-standard"]);

  const combined = new Set();
  for (const chunk of [committedOrStagedOrUnstaged, unstaged, staged, untracked]) {
    if (!chunk) continue;
    for (const filePath of toSet(chunk)) {
      if (isGitIgnored(filePath)) {
        continue;
      }
      combined.add(filePath);
    }
  }

  return combined;
}

function main() {
  const cwd = process.cwd();
  const config = loadConfig(cwd);
  const baseRef = assertBaseRef(config.baseRef);
  const mergeBase = runGit(["merge-base", "HEAD", baseRef]);
  const candidates = collectCandidatePaths(mergeBase);

  const violations = [];
  const warnings = [];

  for (const filePath of candidates) {
    const isExisting = fileExistsInRef(baseRef, filePath);

    if (isExisting) {
      const allowedExact = config.allowedModifiedFiles.includes(filePath);
      const allowedByGlob = matchesAnyGlob(filePath, config.allowedModifiedFileGlobs);
      if (!allowedExact && !allowedByGlob) {
        violations.push(
          `Modified upstream file not in whitelist: ${filePath}. Add it to allowedModifiedFiles only if absolutely required.`,
        );
      } else {
        const autoMergeViolation = maybeCheckWhitelistedAutoMerge({
          baseRef,
          mergeBase,
          filePath,
        });
        if (autoMergeViolation) {
          violations.push(autoMergeViolation);
        }
      }
      continue;
    }

    const baseName = path.basename(filePath);
    const hasRequiredPrefix = config.requiredPrefixes.some((prefix) => baseName.startsWith(prefix));
    const allowedUnprefixed = matchesAnyGlob(filePath, config.allowedUnprefixedNewFiles);
    if (!hasRequiredPrefix && !allowedUnprefixed) {
      violations.push(
        `New file must use one of prefixes [${config.requiredPrefixes.join(", ")}]: ${filePath} (or add a specific allow pattern).`,
      );
    }

    if (shouldCheckLoc(filePath, config.requiredPrefixes)) {
      const loc = countNonEmptyLines(filePath);
      const counterpartPath = resolveUpstreamCounterpartPath(
        baseRef,
        filePath,
        config.requiredPrefixes,
      );
      const result = classifyPrefixedLocResult({
        filePath,
        loc,
        locWarnThreshold: config.locWarnThreshold,
        locFailThreshold: config.locFailThreshold,
        counterpartPath,
      });
      if (result?.kind === "violation") {
        violations.push(result.message);
      } else if (result?.kind === "warning") {
        warnings.push(result.message);
      }
    }
  }

  if (violations.length > 0) {
    console.error("t3work additive guard failed\n");
    for (const violation of violations) {
      console.error(`- ${violation}`);
    }

    if (warnings.length > 0) {
      console.error("\nWarnings:");
      for (const warning of warnings) {
        console.error(`- ${warning}`);
      }
    }

    process.exit(1);
  }

  if (warnings.length > 0) {
    console.warn("t3work additive guard warnings\n");
    for (const warning of warnings) {
      console.warn(`- ${warning}`);
    }
    console.warn("");
  }

  console.log(
    `t3work additive guard passed (${candidates.size} changed files checked against ${baseRef}).`,
  );
}

function isCliEntry() {
  const scriptPath = process.argv[1];
  if (!scriptPath) return false;
  return import.meta.url === pathToFileURL(scriptPath).href;
}

if (isCliEntry()) {
  main();
}
